import { getTotalViewScrolled } from './view';

describe('ViewUtilities', () => {
  describe('getScrollViewPosition', () => {
    it('scrolled in a 1024 to 768 screen', () => {
      global.innerHeight = 768;
      global.innerWidth = 1024;
      const result = getTotalViewScrolled({ x: 0, y: 0 });

      expect(result).toStrictEqual({ x: 1024, y: 768 });
    });

    it('scrolled 1000px in Y in a 1024 to 768 screen', () => {
      global.innerHeight = 768;
      global.innerWidth = 1024;
      const result = getTotalViewScrolled({ x: 0, y: 1000 });

      expect(result).toStrictEqual({ x: 1024, y: 1768 });
    });
  });
});
